package com.datalogic.decode;

/**
 * <code>PresentationState</code> is an enumeration class defining constants for presentation mode states.
 * 
 */
public enum PresentationState {
    /*
    Ready state
    */
    READY,
    /*
    Waiting state
    */
    WAITING,
    /*
    Scanning state
    */
    SCANNING,
    /*
    Presentation mode disabled state
    */
    DISABLED;
}