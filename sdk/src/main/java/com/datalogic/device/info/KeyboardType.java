package com.datalogic.device.info;

/**
 * The <code>KeyboardType</code> enumeration lists keyboard type codes.
 */
public enum KeyboardType {
	/** Unit has no keyboard attached */
	NONE,
	/** Numeric keyboard */
	NUMERIC,
	/** Alpha Numeric keyboard */
	ALPHA_NUMERIC,
	/** Qwerty keyboard */
	QWERTY,
	/** E-Business keyboard */
	E_BUSINESS,
	/** Full Alpha Numeric keyboard */
	FULL_ALPHA_NUMERIC;
}
