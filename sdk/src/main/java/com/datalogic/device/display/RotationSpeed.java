package com.datalogic.device.display;

/**
 * The <code>RotationSpeed</code> enumeration defines the possible notification rotation speed on 2nd display.
 */
public enum RotationSpeed {
    /** Low speed */
    SLOW,
    /** Medium speed */
    MEDIUM,
    /** High speed */
    FAST;

    /**
     * @hide
     */
    private static RotationSpeed[] allValues = values();

}
